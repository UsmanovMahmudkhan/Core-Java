package Basic_problems;

public class American_Flag {
    /**
     * Write a Java program to print an American flag on the screen.
     * Expected Output
     *      12                34
     * * * * * * * ==================================
     *  * * * * *  ==================================
     * * * * * * * ==================================
     *  * * * * *  ==================================
     * * * * * * * ==================================
     9  * * * * *  ==================================
     * * * * * * * ==================================
     *  * * * * *  ==================================
     * * * * * * * ==================================
     * ==============================================
     * ==============================================
     * ==============================================
     * ==============================================  15
     * ==============================================
     * ==============================================
     */

    public static void main(String[] args) {
        //rendering column
        for(int i=0;i<24;i++){
            //rendering row
            for(int j=0;j<46;j++){
                if(i<9 && j<12) {
                    if (i % 2 == 0 && j % 2 == 0) {
                        System.out.print("*");
                    } else if (i % 2 == 1 && j % 2 == 1) {
                        System.out.print("*");
                    } else {
                        System.out.print(" ");
                    }
                }
                else {
                    System.out.print("=");
                }
            }
            System.out.println();
        }
    }


}
