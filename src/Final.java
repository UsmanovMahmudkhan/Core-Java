public class Final {

    public static void main(String[] args) {
        final int AGE=12;
        final String NAME="Alex";

        /** so when final keyboard is used,
         * we cant change its value, we can use final in method and variable, classes.
         * It is better to declare final variable in uppercase
         **/


        System.out.println("Name: "+NAME+" Age: "+AGE);


    }
}
